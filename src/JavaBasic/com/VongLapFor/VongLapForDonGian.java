package JavaBasic.com.VongLapFor;

public class VongLapForDonGian {
    public static void main(String[] args) {
        for (int i = 1; i<20 ; i = i+2){
            System.out.println("Số thứ tự i:" + i );
            System.out.println("Selenium");
        }
        for (int j = 1; j <= 30; j++){
            if (j % 2 ==0){
                System.out.println("Số chia hết cho 2: " +j);
            }
        }
    }
}
